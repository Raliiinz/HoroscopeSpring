CREATE TABLE stones (
                        stone_id serial PRIMARY KEY,
                        stone_name varchar(100) NOT NULL,
                        description text,
                        image_path text
);

CREATE TABLE zodiac_stones (
                               zodiac_sign_id INT NOT NULL,
                               stone_id INT NOT NULL,
                               PRIMARY KEY (zodiac_sign_id, stone_id),
                               CONSTRAINT FK_zodiac_stone_sign FOREIGN KEY (zodiac_sign_id)
                                   REFERENCES zodiac_signs(sign_id),
                               CONSTRAINT FK_zodiac_stone_stone FOREIGN KEY (stone_id)
                                   REFERENCES stones(stone_id)
);

